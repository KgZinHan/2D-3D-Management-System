package com.entity;

public class ColorCount2D {

	private int greenCount;
	private int blueCount;
	private int blackCount;
	private int orangeCount;
	private int redCount;
	private int purpleCount;
	public int getGreenCount() {
		return greenCount;
	}
	public void setGreenCount(int greenCount) {
		this.greenCount = greenCount;
	}
	public int getBlueCount() {
		return blueCount;
	}
	public void setBlueCount(int blueCount) {
		this.blueCount = blueCount;
	}
	public int getBlackCount() {
		return blackCount;
	}
	public void setBlackCount(int blackCount) {
		this.blackCount = blackCount;
	}
	public int getOrangeCount() {
		return orangeCount;
	}
	public void setOrangeCount(int orangeCount) {
		this.orangeCount = orangeCount;
	}
	public int getRedCount() {
		return redCount;
	}
	public void setRedCount(int redCount) {
		this.redCount = redCount;
	}
	public int getPurpleCount() {
		return purpleCount;
	}
	public void setPurpleCount(int purpleCount) {
		this.purpleCount = purpleCount;
	}
		
}
