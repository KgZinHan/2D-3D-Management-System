package com.entity;

public class Comm2D {
	private String commName;
	private int commPercent;
	private int commZ;
	private String commRemark;
	private int commLimit;
	private String partition;

	public String getCommName() {
		return commName;
	}

	public void setCommName(String commName) {
		this.commName = commName;
	}

	public int getCommPercent() {
		return commPercent;
	}

	public void setCommPercent(int commPercent) {
		this.commPercent = commPercent;
	}

	public int getCommZ() {
		return commZ;
	}

	public void setCommZ(int commZ) {
		this.commZ = commZ;
	}

	public String getCommRemark() {
		return commRemark;
	}

	public void setCommRemark(String commRemark) {
		this.commRemark = commRemark;
	}

	public int getCommLimit() {
		return commLimit;
	}

	public void setCommLimit(int commLimit) {
		this.commLimit = commLimit;
	}

	public String getPartition() {
		return partition;
	}

	public void setPartition(String partition) {
		this.partition = partition;
	}

}
